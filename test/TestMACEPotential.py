test_data_dir = os.path.join(os.path.dirname(os.path.abspath(__file__)), "data")
# Keep this file scoped to MACE-OFF.  MACE-LES has additional long-range
# fixtures in TestMACELESPotential.py.
models = [name for name, path in MACE_OFF_MODEL_PATHS.items() if path.is_file()]
# Maximum measured PyTorch-CUDA versus JAX/XLA float64 force delta is
# 2.95e-11 kJ/mol/angstrom for the periodic fixture.
FORCE_ATOL = 5e-11


class TestMACE:
    @pytest.mark.parametrize("model", models)
    def testFloat64CheckpointAndLoad(self, model):
        checkpoint = MACE_OFF_MODEL_PATHS[model]
        with checkpoint.open("rb") as handle:
            json.loads(handle.readline().decode("utf-8"))
            stored_dtypes = []
            while handle.tell() < checkpoint.stat().st_size:
                stored_dtypes.append(np.load(handle).dtype)
        assert stored_dtypes
        assert set(stored_dtypes) == {np.dtype(np.float64)}

        loaded_model = load_model(model, dtype=jnp.float64)
        floating_dtypes = {
            leaf.dtype
            for leaf in jax.tree_util.tree_leaves(loaded_model)
            if hasattr(leaf, "dtype") and jnp.issubdtype(leaf.dtype, jnp.floating)
        }
        assert floating_dtypes == {jnp.dtype(jnp.float64)}

    @pytest.mark.parametrize("model", models)
    def testCreatePureMLSystem(self, model):
        pdb = app.PDBFile(os.path.join(test_data_dir, "toluene", "toluene.pdb"))
        potential = MLPotential(model)
        positions = pdb.getPositions(asNumpy=True)
        system = potential.createSystem(pdb.topology, returnEnergyType="energy", preprocessing_positions=positions, use_float64=True)
        context = mm.Context(system, mm.VerletIntegrator(0.001), cuda_platform, {"DeviceIndex": "0", "Precision": "double"})
        context.setPositions(positions)
        state = context.getState(energy=True, forces=True)
        energyML = state.getPotentialEnergy().value_in_unit(unit.kilojoules_per_mole)
        forcesML = state.getForces(asNumpy=True).value_in_unit(unit.kilojoules_per_mole / unit.angstrom)
        # Reference energies are calculated with MACECalculator
        refEnergy = ENERGIES["toluene"]
        refForces = FORCES["toluene"]
        np.testing.assert_allclose(refEnergy[model], energyML, rtol=1e-10)
        np.testing.assert_allclose(refForces[model], forcesML, rtol=1e-10, atol=FORCE_ATOL)

    def testPeriodicSystem(self):
        model = "mace-jax-off-s-23"
        pdb = app.PDBFile(os.path.join(test_data_dir, "alanine-dipeptide", "alanine-dipeptide-explicit.pdb"))
        potential = MLPotential(model)
        positionsOriginal = pdb.getPositions(asNumpy=True)
        system = potential.createSystem(pdb.topology, returnEnergyType="energy", preprocessing_positions=positionsOriginal, use_float64=True)
        context = mm.Context(system, mm.VerletIntegrator(0.001), cuda_platform, {"DeviceIndex": "0", "Precision": "double"})
        energyRef = ENERGIES["alanine-dipeptide-explicit"][model]  # Calculated with MACECalculator
        forcesRef = FORCES["alanine-dipeptide-explicit"][model]
        for i in range(3):
            positions = positionsOriginal + i * 0.9 * unit.nanometers
            context.setPositions(positions)
            state = context.getState(getEnergy=True, getForces=True)
            energyML = state.getPotentialEnergy().value_in_unit(unit.kilojoules_per_mole)
            forcesML = state.getForces(asNumpy=True).value_in_unit(unit.kilojoules_per_mole / unit.angstrom)
            assert np.isclose(energyRef, energyML, rtol=1e-10)
            np.testing.assert_allclose(forcesRef, forcesML, rtol=1e-10, atol=FORCE_ATOL)

