    @pytest.mark.parametrize("model", ["fennix-bio1-small", "fennix-bio1-medium"])
    def testCreatePureMLSystem(self, model):
        pdb = app.PDBFile(os.path.join(test_data_dir, "toluene", "toluene.pdb"))
        potential = MLPotential(model)
        positions = pdb.getPositions(asNumpy=True)
        system = potential.createSystem(pdb.topology, precision="double", preprocessing_positions=positions)
        context = mm.Context(system, mm.VerletIntegrator(0.001), cuda_platform, {"DeviceIndex": "0", "Precision": "double"})
        context.setPositions(positions)
        state = context.getState(energy=True, forces=True)
        energyML = state.getPotentialEnergy().value_in_unit(unit.kilojoules_per_mole)
        forcesML = state.getForces(asNumpy=True).value_in_unit(unit.kilojoules_per_mole / unit.nanometer)
        # Reference energies are calculated with FENNIXCalculator
        refEnergy = ENERGIES["toluene"]
        refForces = FORCES["toluene"]
        np.testing.assert_allclose(refEnergy[model], energyML, rtol=1e-5)
        np.testing.assert_allclose(refForces[model], forcesML, rtol=1e-5)

